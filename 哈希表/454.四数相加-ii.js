/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @param {number[]} nums3
 * @param {number[]} nums4
 * @return {number}
 */
var fourSumCount = function (nums1, nums2, nums3, nums4) {
  const map1 = {}
  for (let i = 0; i < nums1.length; i++) {
    for (let j = 0; j < nums2.length; j++) {
      const sum = nums1[i] + nums2[j]
      map1[sum] = map1[sum] || 0
      map1[sum]++
    }
  }
  const map2 = {}
  for (let i = 0; i < nums3.length; i++) {
    for (let j = 0; j < nums4.length; j++) {
      const sum = nums3[i] + nums4[j]
      map2[sum] = map2[sum] || 0
      map2[sum]++
    }
  }

  let finalArrLen = 0
  for (let key in map1) {
    const target = 0 - key
    if (map2[target]) {
      finalArrLen += map1[key] * map2[target]
    }
  };
  return finalArrLen
}
